package day3_operators;

public class AssignmentOperator {
    public static void main(String[] args){
//        assignment operators
        int a=10;
        //a=a+5;
        a+=5;
        System.out.println(a);
        a-=4;
        System.out.println(a);
        a*=2;
        System.out.println(a);
        a/=4;
        System.out.println(a);
    }
}
